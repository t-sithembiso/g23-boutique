package za.ac.cput.factory;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import za.ac.cput.domain.Receptionist;
import za.ac.cput.domain.Staff;

import java.util.HashSet;
import java.util.Set;

import static org.junit.jupiter.api.Assertions.*;

class ReceptionistFactoryTest {

    private Set<Staff> testStaff;

    @BeforeEach
    void setUp() {
        // Set up any necessary test data before each test
        testStaff = new HashSet<>();
        testStaff.add(new Staff.Builder()
                .setStaffNumber(1)
                .setNationalId(123456789)
                .setStaffName("Okuhle")
                .setStaffSurname("Sithole")
                .build());
    }

    @Test
    void createReceptionist() {
        // Define test data
        int receptionistId = 1;
        String username = "receptionist";
        String password = "pass123";

        // Create a Receptionist using the factory
        Receptionist receptionist = ReceptionistFactory.createReceptionist(receptionistId, username, password, testStaff);

        // Print the created receptionist details
        System.out.println("Created Receptionist: " + receptionist);

        // Verify that the Receptionist is not null
        assertNotNull(receptionist);

        // Verify that the Receptionist properties are set correctly
        assertEquals(receptionistId, receptionist.getReceptionist_id());
        assertEquals(username, receptionist.getUsername());
        assertEquals(password, receptionist.getPassword());
        assertEquals(testStaff, receptionist.getStaff());

        // Print assertion results
        System.out.println("Receptionist ID: " + receptionist.getReceptionist_id());
        System.out.println("Receptionist Username: " + receptionist.getUsername());
        System.out.println("Receptionist Password: " + receptionist.getPassword());
        System.out.println("Receptionist Staff: " + receptionist.getStaff());
    }
}
